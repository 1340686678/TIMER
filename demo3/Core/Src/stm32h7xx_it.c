//定时器溢出回调函数
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)	
{
  if(htim->Instance == TIM1){
		//100KHZ
		//每次溢出时间为10us
    if(count_temp.flg==1)//还未成功捕获
    {
				if(count_temp.num_period==0XFFFF)//电平太长了
				{
						count_temp.flg=2;        //标记成功捕获了一次
						count_temp.num=0XFFFF;
				}
				else 
					count_temp .num_period ++;
    }
  }
}

//捕获中断回调函数
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)		
{
  if(htim->Instance == TIM1){
		if ( count_temp.flg == 0 )
		{   
				// 清零定时器计数
				__HAL_TIM_SET_COUNTER(htim,0); 
				//设置下升沿触发
				__HAL_TIM_SET_CAPTUREPOLARITY(&htim1, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_FALLING );
				count_temp .flg = 1;    //设置已经开始    
				count_temp .num_period = 0;    //溢出计数清零        
				count_temp .num = 0; //计数清零
		}        
		else
		{
				// 获取定时器计数值
				count_temp .num = HAL_TIM_ReadCapturedValue(&htim1,TIM_CHANNEL_1);
				//设置下降沿触发
				__HAL_TIM_SET_CAPTUREPOLARITY(&htim1, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_RISING );
				count_temp .flg = 2;
		}
  }
}
